<p><strong><a name="f10" id="f10"></a>O que é a Restituição de Imposto de Renda (tax income)?</strong></p>
<p>&Eacute; o reembolso dos impostos pagos em excesso durante o ano fiscal (6 Abril - 5 Abril ). São várias as circuntancias em que os impostos poderão ser cobrados erroneamente. Mas é sua a responsabilidade de conferir e exigir o seu reembolso. </p>

<p><strong><a name="f11" id="f11"></a>Eu posso receber todo o valor de impostos que eu paguei?</strong></p>
<p>Se o total dos seus ganhos foram menor que o limite de isenção anual (£6035 - 2008/09) então você receberá (100%) tudo que pagou de impostos. 
</p>

<p><strong><a name="f12" id="f12"></a>Eu tenho direto ao reembolso?</strong></p>
<p>Qualquer contribuinte que trabalhou ou est&aacute; trabalhando no Reino Unido, independentemente da situa&ccedil;&atilde;o no pa&iacute;s. O direito tamb&eacute;m se estende aqueles que j&aacute; retornaram aos seus pa&iacute;ses de origem. &Eacute; quase certo que voc&ecirc; tenha direito, se enquadrar e UM dos seguintes crit&eacute;rios:</p>
<ol>
    <li>Se voc&ecirc; n&atilde;o trabalhou todo o  ano fiscal (6/abril at&eacute; 5/abril do ano seguinte);</li>
    <li>Seus ganhos durante o ano fiscal foram  menos  &pound;6,035; </li>
    <li>Voc&ecirc; foi registrado no c&oacute;digo fiscal errado; </li>
        <li>Voc&ecirc; está no código fiscal de emergência; </li>

    <li>Voc&ecirc; trabalha na constru&ccedil;&atilde;o (CIS) ou;</li>
    <li>Voc&ecirc; &eacute; estudante. </li>
</ol>

<p><strong><a name="f13" id="f13"></a>Como eu sei se estou pagando o código fiscal de emergência?</strong></p>
<p>Se há a sufixo ou prefixo no código fiscal. Os mais comuns são – W1, M1, BR, X, WK1, MTN1. O código fiscal padrão para o ano fiscal de 2008/09  é 603L. Em algumas circunstâncias especiais um diferente código fiscal é aplicável. </p>



<p><strong><a name="f14" id="f14"></a>Quando posso solicitar minha restiuição do Imposto de Renda?</strong></p>
<p>Sempre ao t&eacute;rmino do ano fiscal (depois de 05 de abril) ou ao final do vínculo empregatício quando não se pretende voltar a trabalhar no mesmo ano fiscal.</p>

<p><strong><a name="f15" id="f15"></a>Qual o prazo limite para solicitação da restitui&ccedil;&atilde;o?</strong></p>
<p> A  restitui&ccedil;&atilde;o de impostos pode ser referente aos últimos 6 anos. </p>

<p><strong><a name="f16" id="f16"></a>O que é necessário para solicitar minha restitui&ccedil;&atilde;o?</strong></p>
<p>Somente o formulário original P60 ou P45 de cada empregador/ag&ecirc;ncia que voc&ecirc; trabalhou durante o ano fiscal (Estes formulários podem ser obtidos através do seu empregador). E também assinar um conjunto de formulários declarando vários fatos do seu histórico trabalhista. <a href="getting_started.php">Saiba mais  </a></p>

<p><strong><a name="f17" id="f17"></a>O que fazer no caso de perda dos meus formul&aacute;rios de P60 e P45?</strong></p>
<p>Em caso de perda dos formulários P45 ou P60,  um comprovante de renda do seu empregador, conhecido como <strong>&quot;Statement of Earnings&quot;</strong>, poder&aacute; ser usado como substituto. Se voc&ecirc; é autônomo (CIS sub-contractor) ou trabalha terceirizado, voce precisará de todos os seus payslips.
<p>Aten&ccedil;&atilde;o: HM Revenue &amp; Customs n&atilde;o aceita payslips como substituto do P45 ou P60.</p>

<p><strong><a name="f18" id="f18"></a>Qual a diferença entre P60,  P45 e CIS Statement?</strong></p>
<p>Um  P60 é um certificado de seus rendimentos para o ano fiscal indicando o  quanto voce ganhou e o quanto de impostos foram recolhidos sobre seus  rendimentos. Seu empregador é obrigado a emití-lo até 31 de maio ao  final de cada ano fiscal. O P45 é a declaração de impostos e rendimentos dentro do ano fiscal até a data que você se desligou da empresa. O CIS Statment é emitido mensalmente para quem trabalha na construção. 
</p>

<p><strong><a name="f19" id="f19"></a>O que é Ano Fiscal?</strong></p>
<p>Ano Fiscal é o período compreendido entre 06 de Abril de um ano até 05 de Abril do ano posterior. Por exemplo o ano fiscal para: 2006/07 - começou no dia 06 Abril de 2006 e terminou no dia 05 Abril de 2007.</p>

<p><strong><a name="f110" id="f110"></a>Eu preciso do National Insurance Number (NINO) ou visto (work permit) para receber o meu reembolso?</strong></p>
<p>Não. O HM Revenue & Customs usa seu nome e data de nascimento para localizar suas contribuições. Mas todo  trabalhador no Reino Unido deve procurar o DSS: órgao público fiscalizador de aposentadorias para a obten&ccedil;&atilde;o do NINO definitivo.</p>

<p><strong><a name="f111" id="f111"></a>Eu já fui embora para o meu país de origem. Como posso receber o meu reembolso?</strong></p>
<p>Simplesmente faça o DOWNLOAD do conjunto de formulários,  preencha somente o questionário (primeira página), assine os demais formulários e envie juntamente com os originais do P60 ou P45  para TAX RETURNS ACCOUNTANTS. &nbsp;<a href="getting_started.php" >Saiba mais</a> </p>

<p><strong><a name="f112" id="f112"></a>Eu trabalho na construção no sistema CIS (Construction Industry Scheme). Eu posso reivindicar o reembolso de impostos pagos?</strong></p>
<p>Sim. No sistema CIS, você é tributado 20 por cento e não tem limite anual de isenção. Você  pode reivindicar o reembolso de todas as suas despesas relacionadas com o seu trabalho, inclusive honorários do contador, viagens, telefone, acomodação, ferramentas e roupas de proteção. </p>

<p><strong><a name="f113" id="f113"></a>Eu não estou no sistema PAYE (Pay as You Earn), como posso ter restituição do Imposto de renda?</strong></p>
<p> Se você é um trabalhador autônomo, diretor de empresa ou trabalha na construção no sistema CIS, você deve enviar sua declaração anual de impostos (tax return) para receber o seu reembolso.  Isto  poderá ser efetuado somente uma vez, ao final de cada ano fiscal.</p>

<p><strong><a name="f114" id="f114"></a>Quem é obrigado a efetuar a declaração anual do imposto de renda - Self Assessment?</strong></p>
<p>Para os trabalhadores que estão no sistema PAYE (Pay as You Earn) não é necessário submeter a declaração do imposto de renda (tax returns). O Self Assessment deverá ser efetuado para as seguintes categorias abaixo:</p>
<ul>
    <li>Diretores/Sócios de empresas</li>
    <li>Autônomo</li>
    <li>Trabalhadores da Construção (Construction Industry Scheme - CIS)</li>
    <li>Ministros de Religião</li>
    <li>Proprietarios de Imóveis alugados</li>


</ul>
